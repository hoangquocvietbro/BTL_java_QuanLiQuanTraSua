    private void MouseClick(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MouseClick
        int row= TableTaiKhoan.getSelectedRow();
        String hoten = (String) TableTaiKhoan.getValueAt(row, 0);
        String tk = (String) TableTaiKhoan.getValueAt(row, 1);
        String mk = (String) TableTaiKhoan.getValueAt(row, 2);
        
        Hoten.setText(hoten);
        TaiKhoan.setText(tk);
        MatKhau.setText(mk);
    }//GEN-LAST:event_MouseClick

    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        // lay du lieu
        String result = Hoten.getText() + ";" + TaiKhoan.getText() + ";" + MatKhau.getText()+"\n";
        FileController.AddNew(result, "TaiKhoan.txt");
        DanhSachTaiKhoan.add(new TaiKhoan( Hoten.getText(),TaiKhoan.getText(), MatKhau.getText()));
       model.addRow(new Object[]{
                Hoten.getText() , TaiKhoan.getText(), MatKhau.getText()
            });
        Hoten.setText("");
        TaiKhoan.setText("");
        MatKhau.setText("");
    }//GEN-LAST:event_BtnAddActionPerformed

    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        int mess = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn xóa ?");
        if(mess==JOptionPane.YES_OPTION){
            int row= TableTaiKhoan.getSelectedRow();
            DanhSachTaiKhoan.remove(row);
            model.removeRow(row);
            FileController.UpdateTaiKhoan(DanhSachTaiKhoan);
        } Hoten.setText("");
        TaiKhoan.setText("");
        MatKhau.setText("");
            
           
    }//GEN-LAST:event_BtnDeleteActionPerformed

